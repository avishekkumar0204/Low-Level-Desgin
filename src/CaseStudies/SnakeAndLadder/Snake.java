package CaseStudies.SnakeAndLadder;

public class Snake {
    private int startPoint;
    private int endPoint;

    Snake(int startPoint, int endPoint) {
        this.startPoint = startPoint;
        this.endPoint = endPoint;
    }

    int getStartPoint() {
        return this.startPoint;
    }
    int getEndPoint() {
        return this.endPoint;
    }

}
