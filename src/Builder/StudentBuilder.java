package Builder;

public class StudentBuilder {
    int id;
    String name;
    int age;
    String batch;
    int gradYear;

    public void setId(int id) {
        this.id = id;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public void setBatch(String batch) {
        this.batch = batch;
    }
    public void setGradYear(int gradYear) {
        this.gradYear = gradYear;
    }
}
